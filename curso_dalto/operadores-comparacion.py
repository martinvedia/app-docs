# == -> Igual que
# != -> Es distinto que
# > -> Es mayor que
# < -> Es menor que
# >= -> Es mayor que
# <= -> Es menor que

# Estos operadores devuelven un booleano y se puede comparar datos o variables diferentes
print(f'1 == 3: ', 1 == 3)
print(f'1 != 3: ', 1 != 3)
print(f'1  < 3: ', 1 < 3)
print(f'1  > 3: ', 1 > 3)
print(f'1 <= 3: ', 1 <= 3)
print(f'1 >= 3: ', 1 >= 3)
print( f'"Rigo" == "Berto": ', 'Rigo' == 'Berto')
print( f'"Berto" == "Berto": ', 'Berto' == 'Berto')