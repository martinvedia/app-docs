### Operadores aritmeticos ###
##############################

### Suma y resta ###
unaSuma = 12 + 5
unaResta = 12 - 5
print (f'Suma: {unaSuma} | Resta: {unaResta}')

### Multiplicación y División  ###
unaMultiplicacion = 12 * 5
unaDivision = 12 / 5 # La división devuelve un Float
print(f'Multiplicación: {unaMultiplicacion} | División: {unaDivision}')

### Divisón baja ###
unaDivisionBaja = 12 // 5 # Esta operación devuelve un entero, redondeado hacia abajo
print (f'División baja: {unaDivisionBaja}')

### Exponente ###
unaExponente = 12 ** 5
print(f'Exponente: {unaExponente}')

### Modulo op resto ###
unResto = 12 % 5
print(f'Resto: {unResto}')
